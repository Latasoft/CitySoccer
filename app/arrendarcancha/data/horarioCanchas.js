// Funciones helper para obtener precios
function obtenerTipoDia(fecha) {
  const dia = fecha.getDay(); // 0 = domingo, 1 = lunes, ..., 6 = sábado
  
  if (dia === 0) return 'domingo_festivos'; // domingo
  if (dia === 6) return 'sabado'; // sábado
  return 'lunes_viernes'; // lunes a viernes
}

function obtenerPrecio(tipoCancha, modalidad, fecha, horaInicio, horaFin) {
  const tipoDia = obtenerTipoDia(fecha);
  
  let horarios;
  if (tipoCancha === 'pickleball') {
    horarios = PRECIOS_CANCHAS[tipoCancha][modalidad][tipoDia];
  } else {
    horarios = PRECIOS_CANCHAS[tipoCancha][tipoDia];
  }
  
  const horario = horarios.find(h => 
    h.hora_inicio === horaInicio && h.hora_fin === horaFin
  );
  
  return horario ? horario.precio : null;
}

function obtenerHorariosDisponibles(tipoCancha, modalidad, fecha) {
  const tipoDia = obtenerTipoDia(fecha);
  
  if (tipoCancha === 'pickleball') {
    return PRECIOS_CANCHAS[tipoCancha][modalidad][tipoDia];
  } else {
    return PRECIOS_CANCHAS[tipoCancha][tipoDia];
  }
}

